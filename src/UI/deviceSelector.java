    /** Creates new form deviceSelector */
    public deviceSelector(mainFrame frm) {
        this.frm = frm;
        initComponents();
        Mixer.Info[] inf = AudioSystem.getMixerInfo();
        for (int x = 0; x < inf.length; x++) {
            if (inf[x].getDescription().toLowerCase().contains("capture")) {
                this.jComboBoxRecord.addItem(inf[x]);
                if (inf[x].hashCode() == frm.getRecorder().getMixerInfo().hashCode()) {
                    this.jComboBoxRecord.setSelectedItem(inf[x]);
                }
            } else {
                this.jComboBoxPlay.addItem(inf[x]);
                if (inf[x].hashCode() == frm.getPlayer().getMixerInfo().hashCode()) {
                    this.jComboBoxPlay.setSelectedItem(inf[x]);
                }
            }
        }
    }
    
